#include<bits/stdc++.h>
using namespace std;
void oneDArray() {
     for (int i=1; i<=10;i++){
        cout <<i <<endl;
    }
}

void twoDArray() {
    for (int i=0;i<3;i++ ){
        for(int j=0;j<3;j++){
            cout << i<<" "<<j << endl;
        }
    }
}
int main() {
    // 1-D Array
     oneDArray();
   

    // 2-D Array
    twoDArray();

}